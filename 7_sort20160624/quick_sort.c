/*-----------------------------------------------------------*
 * 函数实现                                                  *
 *-----------------------------------------------------------*/
void quick_sort(int *array, int begin, int end)
{
     int    key = array[begin];
     int    i = begin;
     int    j = end;
     int    temp = 0;

     if (array == NULL)
     {
         printf("快排数组不存在!\n");
         return ;
     }

     if (begin >= end)
     {
          return;
     }

     while(1)
     {
         while(array[j] >= key && i < j)
         {
             j--;
         }

         while(array[i] <= key && i < j)
          {
              i++;
          }

          if (i < j)
          {
               temp = array[i];
               array[i] = array[j];
               array[j] = temp;
          }
          else
          {
               break;
          }
    }
     temp = array[j];
     array[j] = array[begin];
     array[begin] = temp;

     quick_sort(array, begin, j-1);
     quick_sort(array, j+1, end);

}
