#include <stdio.h>
#include <stdlib.h>

int u[6] = {1,1,1,2,3,4};
int v[6] = {2,3,5,4,5,5};
int w[6] = {2,5,3,5,5,2};

int cmp(const void *i, const void *j)
{
    int a = *(int*)i;
    int b = *(int*)j;
    return w[a] > w[b];
}

int main(void)
{
    int r[6] = {0};
    int i = 0;

    for (i = 0; i < 6; i++)
    {
         r[i] = i;
    }

    qsort(r, 6, sizeof(int), cmp);

    for(i = 0; i < 6; i++)
    {
         printf("r[i] = %d, u[r[i]] = %d,"
                 "v[r[i]] = %d, w[r[i]] = %d\n",
                 r[i], u[r[i]], v[r[i]], w[r[i]]);
    }

}
